package Javaexp.a06_objectreview.vo;

public class Office {
	
	String loc;
	int capcity;
	public Office() {
	}
	public Office(String loc, int capcity) {
		this.loc = loc;
		this.capcity = capcity;
	}
	public String getLoc() {
		return loc;
	}
	public void setLoc(String loc) {
		this.loc = loc;
	}
	public int getCapcity() {
		return capcity;
	}
	public void setCapcity(int capcity) {
		this.capcity = capcity;
	}
	
	

}
